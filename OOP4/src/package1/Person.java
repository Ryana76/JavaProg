package package1;

public class Person {
	
	private String name;
	private String family;
	private int age;
	private double avg;
	
	
	public Person(String name, String family, int age, double avg) {
		this.name = name;
		this.family = family;
		this.age = age;
		this.avg = avg;
	}
	
	
	public Person() {
	}


	public Person(String name) {
		this.name = name;
	}
	
	
	

}
