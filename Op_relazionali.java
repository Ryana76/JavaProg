public class Op_relazionali {
    public static void main(String[] args){

        int num1 , num2;
        num1 = 3;
        num2 = 5;

        System.out.println("num1 is : " + num1 + "num2 is : " + num2);

        System.out.println(num1 == num2);       //Falso
        System.out.println(num1 != num2);       //Vero
        System.out.println(num1 >= num2);       //Falso
        System.out.println(num1 <= num2);       //Vero
        System.out.println(num1 > num2);       //Falso
        System.out.println(num1 < num2);       //Vero

    }
}
